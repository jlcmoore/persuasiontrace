"""Plot and summarize uncertainty for counterfactual replay corpora.

This script reads ``*_round_errors.csv`` or ``*_round_errors.jsonl`` generated by
``analysis.simulator_counterfactual_replay`` and writes:

- ``<prefix>_comparison_plot.{png,pdf}``: corpus comparison chart with 95% bootstrap CIs.
- ``<prefix>_target_error_plot.{png,pdf}``: per-corpus target-error means with 95% CIs.
- ``<prefix>_node_error_plot.{png,pdf}``: per-corpus node-error means with 95% CIs.
- ``<prefix>_comparison_with_ci.csv``: per-corpus means and 95% bootstrap CIs.
- ``<prefix>_pairwise_target_error.csv``: paired corpus comparisons on
  ``final_target_abs_error`` including approximate sample-size guidance.
- ``<prefix>_human_loo_plot.{png,pdf}``: human-vs-human leave-one-out comparison
  for unconditional vs same-bin conditional prediction.
- ``<prefix>_conditional_score_divergence_plot.{png,pdf}``: canonical strict
  conditional core average-error ranking (lowest to highest, no CIs).
- ``<prefix>_conditional_core_avg_error_loo_bins_plot.{png,pdf}``: strict
  apples-to-apples core error chart restricted to human-LOO evaluable bins.
- ``<prefix>_conditional_core_avg_error_bin_sensitivity_summary.csv``:
  strict conditional core-error summaries under exact/coarse human bins.

When logical, emotional, and authoritarian simulated-target corpora are present,
the script also appends a derived persona-average corpus row
(``logos_pathos_ethos_average``) built by averaging the three personas
per source round.
"""

